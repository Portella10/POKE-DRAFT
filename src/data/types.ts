/** Single-type multiplier (defaults to neutral 1). */
function pairMultiplier(attack: TypeName, defend: TypeName): number {
  return CHART[attack][defend] ?? 1;
}

/** Combined effectiveness of a move type against a (possibly dual-type) defender. */
export function effectiveness(moveType: TypeName, defenderTypes: TypeName[]): number {
  return defenderTypes.reduce((mult, def) => mult * pairMultiplier(moveType, def), 1);
}

/** Best effectiveness an attacker's own types can manage against the defender. */
export function typeAdvantage(attackerTypes: TypeName[], defenderTypes: TypeName[]): number {
  return attackerTypes.reduce(
    (best, att) => Math.max(best, effectiveness(att, defenderTypes)),
    0,
  );
}
